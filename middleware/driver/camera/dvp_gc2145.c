bool gc2145_detect(const dvp_camera_i2c_callback_t *cb)
{
	uint8_t hb_id = 0, lb_id;

	SENSOR_I2C_READ(CHIP_ID_ADDR_HB, &hb_id);
	SENSOR_I2C_READ(CHIP_ID_ADDR_LB, &lb_id);

	LOGI("%s, id: 0x%02X%02X\n", __func__, hb_id, lb_id);

	if (hb_id == CHIP_ID_VAL_HB
	    && lb_id == CHIP_ID_VAL_LB)
	{
		LOGI("%s success\n", __func__);
		return true;
	}

	return false;
}

void gc2145_read_register(const dvp_camera_i2c_callback_t *cb, uint8_t addr, uint8_t data)
{
	if (gc2145_read_flag)
	{
		uint8_t value = 0;
		rtos_delay_milliseconds(2);
		SENSOR_I2C_READ(addr, &value);
		if (value != data)
		{
			LOGI("0x%02x, 0x%02x-0x%02x\r\n", addr, data, value);
		}
	}
}

int gc2145_init(const dvp_camera_i2c_callback_t *cb)
{
	uint32_t size = sizeof(sensor_gc2145_init_talbe) / 2, i;

	LOGI("%s\n", __func__);

	for (i = 0; i < size; i++)
	{
		SENSOR_I2C_WRITE(sensor_gc2145_init_talbe[i][0], sensor_gc2145_init_talbe[i][1]);

		gc2145_read_register(cb, sensor_gc2145_init_talbe[i][0], sensor_gc2145_init_talbe[i][1]);
	}

	return 0;
}


void gc2145_set_window_size(const dvp_camera_i2c_callback_t *cb, int w, int h)
{
	uint8_t i = 0;
	uint16_t row_s = (1200 - h) / 2;
	uint16_t col_s = (1600 - w) / 2;
	u32 size = 0;

	uint16_t win_w = 1600;
	uint16_t win_h = 1200;
	const struct subsample_cfg *cfg = NULL;

	for (i = 1; i < sizeof(subsample_cfgs) / sizeof(struct subsample_cfg); i++)
	{
		cfg = &subsample_cfgs[i];

		if ((win_w * cfg->ratio_numerator / cfg->ratio_denominator >= w)
		    && (win_h * cfg->ratio_numerator / cfg->ratio_denominator >= h))
		{
			win_w = w * cfg->ratio_denominator / cfg->ratio_numerator;
			win_h = h * cfg->ratio_denominator / cfg->ratio_numerator;
			row_s = (1600 - win_h) / 2;
			col_s = (1200 - win_w) / 2;
			break;
		}
	}

	LOGI("%d %d %d %d %d ", col_s, row_s);

#define H8(v) ((v)>>8)
#define L8(v) ((v)&0xff)

	uint8_t gc_2145_sum[][2] =
	{
		{ 0xfe, 0x00},
		{ 0X90, 0x01},
		{ 0x09, 0x00},
		{ 0x0a, 0x00},
		{ 0x0b, 0x00},
		{ 0x0c, 0x00},

		{ 0x99, cfg->reg0x99},
		{ 0x9b, cfg->reg0x9b},
		{ 0x9c, cfg->reg0x9c},
		{ 0x9d, cfg->reg0x9d},
		{ 0x9e, cfg->reg0x9e},
		{ 0x9f, cfg->reg0x9f},
		{ 0xa0, cfg->reg0xa0},
		{ 0xa1, cfg->reg0xa1},
		{ 0xa2, cfg->reg0xa2},

		{ 0x95, H8(h)},
		{ 0x96, L8(h)},
		{ 0x97, H8(w)},
		{ 0x98, L8(w)},
	};

	size = sizeof(gc_2145_sum) / 2;

	for (i = 0; i < size; i ++)
	{
		SENSOR_I2C_WRITE(gc_2145_sum[i][0], gc_2145_sum[i][1]);
	}
}

int gc2145_set_ppi(const dvp_camera_i2c_callback_t *cb, media_ppi_t ppi)
{
	LOGI("%s\n", __func__);

	gc2145_set_window_size(cb, ppi_to_pixel_x(ppi), ppi_to_pixel_y(ppi));

	return BK_OK;
}

